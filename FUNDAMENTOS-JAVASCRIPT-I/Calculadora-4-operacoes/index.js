let num1 = parseFloat(prompt("Informe o primeiro numero:"));
let num2 = parseFloat(prompt("Informe o segundo numero:"));

console.log("A soma de " + num1 + " + " + num2 + " = " + (num1 + num2));
console.log("A subtracao de " + num1 + " - " + num2 + " = " + (num1 - num2));
console.log("A multiplicacao de " + num1 + " * " + num2 + " = " + (num1 * num2));
console.log("A divisao de " + num1 + " / " + num2 + " = " + (num1 / num2));
